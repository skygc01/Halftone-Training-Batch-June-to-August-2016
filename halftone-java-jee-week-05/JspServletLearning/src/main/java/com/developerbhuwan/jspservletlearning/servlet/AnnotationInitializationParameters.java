package com.developerbhuwan.jspservletlearning.servlet;

import java.io.IOException;
import java.io.PrintWriter;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebInitParam;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 *
 * @author Bhuwan Prasad Upadhyay
 */
@WebServlet(name = "InitializationParameters", urlPatterns = {"/initann"},
        initParams = {
            @WebInitParam(name = "ssn", value = "2222"),
            @WebInitParam(name = "phone", value = "5430887")
        }
)
public class AnnotationInitializationParameters extends HttpServlet {

    @Override
    protected void doGet(HttpServletRequest req, HttpServletResponse resp) throws ServletException, IOException {
        PrintWriter writer = resp.getWriter();
        writer.write("ServletInitParm-----");
        writer.write("SSN=" + getServletConfig().getInitParameter("ssn"));
        writer.write("PHONE=" + getServletConfig().getInitParameter("phone"));
        writer.write("ContextParm-----");
        writer.write(getServletContext().getInitParameter("ApplicationName"));
    }

}
