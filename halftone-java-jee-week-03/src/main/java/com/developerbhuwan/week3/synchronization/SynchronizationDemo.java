public class SynchronizationDemo {

    /*
        - block level
        - method method
     */
    public static void main(String[] args) {
        new SynchronizationDemo().runApp();
    }

    private final String hello = "Hello";

    public void runApp() {

        synchronized (hello) {
            System.out.println(hello);
        }

    }

    public synchronized void ok() {
        
    }
}
